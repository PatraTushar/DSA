package BASICS;
public class RelationalOperator {

    public static void main(String[] args) {

        int p=4,q=5;

        System.out.println(p==q);
        System.out.println(p!=q);
        System.out.println(p>q);
        System.out.println(p<q);
        System.out.println(p>=q);
        System.out.println(p<=q);
    }
}
