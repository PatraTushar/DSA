package BASICS;

public class Arithmaticoperator {

    public static void main(String[] args) {

        int p = 10, q = 5;

        System.out.println(p + q);
        System.out.println(p - q);
        System.out.println(p * q);
        System.out.println(p / q);
        System.out.println(p % q);
    }
}
